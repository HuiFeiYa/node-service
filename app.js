const Koa = require('koa')
const app = new Koa()
const cors = require('./utils/cors')
const router = require('./router')
const compress = require('koa-compress')
const bodyParser = require('koa-bodyparser')
app.use(cors())
app.use(bodyParser())
app.use(compress())
router(app)
app.listen(8082, () => {
  console.log('8082')
})
